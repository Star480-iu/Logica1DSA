# Programa Verificador do Dia da Semana

# Criando a variável para armazenar o nome do dia
dia = "Sábado"  # você pode alterar para testar outros dias

# Estrutura condicional para verificar se é final de semana
if dia == "Sábado" or dia == "Domingo":
    print("É final de semana!")
else:
    print("É dia de semana.")
