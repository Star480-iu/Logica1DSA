# Programa Verificador de Senha Simples

# Variável que armazena a senha correta
senha_correta = "12345"

# Variável que armazena a senha digitada pelo usuário
senha_digitada = "12345"  # você pode alterar para testar

# Estrutura condicional SE-SENÃO
if senha_digitada == senha_correta:
    print("Login bem-sucedido!")
else:
    print("Senha incorreta. Tente novamente.")
